package app5.patternProgramming;

public class Pattern5 {

	public static void main(String[] args) 
	{
		int i ,j;
		for(i=1;i<=5;i++) {
			for(j=1;j<=i;j++) {
				// System.out.print(" * " );
				System.out.print(i + " " );
				//System.out.print(j + " " );
			}
			System.out.println();
		}

	}

}
